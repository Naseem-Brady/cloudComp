CREATE DATABASE IF NOT EXISTS csc468;
USE csc468;

CREATE TABLE IF NOT EXISTS cattle (
    id INT AUTO_INCREMENT PRIMARY KEY,
    breed VARCHAR(255),
    age INT,
    weight FLOAT
);

